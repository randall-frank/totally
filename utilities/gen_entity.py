def gen_byte(s):
    b = 0
    p = 1
    for v in s:
        if v == '@':
            b += p
        p = p * 2
    return b

sh = -1
n = 7
for l in s.split():
    if '1248' in l:
        continue
    if n == 7:
        sh += 1
        print(f"* Shift #{sh}")
        print(f"         hex   {0:02x}{0:02x}{0:02x}    ; ")
        n = 0
    b0 = gen_byte(l[0:7])
    b1 = gen_byte(l[7:14])
    b2 = gen_byte(l[14:21])
    print(f"         hex   {b0:02x}{b1:02x}{b2:02x}    ; {l.replace('@','*').replace('-',' ')}")
    n = n + 1
